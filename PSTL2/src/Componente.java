
public class Componente {
	
	String word, token;

	public String getWord() {
		return word;
	}

	public void setWord(String word) {
		this.word = word;
	}

	public String getToken() {
		return token;
	}

	public void setToken(String token) {
		this.token = token;
	}
	
	public Componente (String word, String token){
		this.word = word;
		this.token = token;
	}
	
}
